                            <p class="content pt-3 is-size-5">
                                Finance and infrastructure
                            </p>
                                <ul>
                                    <li>
                                        The Finance and Infrastructure Committee provides an assurance on the development and delivery of
                                        key elements to facilitate research. It is responsible for overseeing the development and
                                        implementation of the financial, estates and digital infrastructure objectives of the RDC. Its aim is to
                                        ensure that the RDC is operating in line with strategic objectives of the Institute.
                                    </li>
                                </ul>
                            <p class="content pt-3 is-size-5">
                                Research Program &amp; Policy Development
                            </p>
                                <ul>
                                    <li>
                                        This committee provides impetus to the research and development activities and to provide guidance,
                                        directions to the research community within the Institute. It has representation from all departments.
                                        The committee is a vibrant entity to discuss and propose R&amp;D policy issues. The members highlight
                                        shortcomings in procedural matters and thus sharpen the performance of the RDC.
                                    </li>
                                    
                                </ul>
                            <p class="content pt-3 is-size-5">
                                Collaboration &amp; Community
                            </p>
                                <ul>
                                    <li>
                                        The main responsibility of this committee is to establish collaboration with other universities, public
                                        and private sectors and identify R&amp;D projects including consultancy services which could be
                                        undertaken at the institution. This committee will foster collaborations for mutual benefits and to
                                        maximize industrial connectivity.
                                    </li>
                                </ul>
                            <p  class="content pt-3 is-size-5">
                                Product Development, Monitoring &amp; Commercialization
                            </p>
                                <ul>
                                    <li>
                                        The committee is a hub for strategic partnerships/ collaborations, industry-institute interface,
                                        sponsored or contract research, new knowledge generation, technology transfer, and
                                        commercialization of research to facilitate innovation, incubation, entrepreneurship and start-up
                                        ventures.
                                    </li>
                                </ul>
                            <p  class="content pt-3 is-size-5">
                                IPR, Legal &amp; Ethical Matters
                            </p>
                                <ul>
                                    <li>
                                        This committee will function with the prime focus of enabling researchers to identify, generate and
                                        protect their intellectual property (IP) through filing procedures for rights like patents, copyrights,
                                        trademarks, designs, etc. This committee envision creating an environment for acquiring new
                                        knowledge through innovation, developing an attitude of prudent IP management practices and
                                        promoting an IPR culture compatible with the educational mission of the academic institution. This
                                        committee also provide advice and guidance to the academic community on all matters pertaining to
                                        academic research ethics.
                                    </li>
                                </ul>                          
