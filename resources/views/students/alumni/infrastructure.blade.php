<p class="subtitle has-text-info">Infrastructure</p>
<p>
    Managing committee will request the management of C. K. Pithawalla Institute of Pharmaceutical Science and Research
    for
    providing the office space with necessary facilities for communication and record storage and space for meeting for
    administering the CKPIPSRAA.
</p>
<p class="subtitle has-text-info">Accounts</p>
<ul>
    <li>Separate account should be open-up in any nationalized bank and maintained in the name of "C. K. Pithawalla
        Institute of Pharmaceutical Science and research Alumni Association" .</li>
    <li>All the financial transaction shall be carryout as per the institute norms.</li>
    <li>Treasurer shall maintain the book of accounts of CKPIPSRAA and shall present the same to the managing committee
        at the end of each financial year for its approval.</li>
</ul>
<p class="subtitle has-text-info">Chairperson at Meeting</p>
<p>
    President of CKPIPSRAA or his or her nominee shall act as a chairperson at all the meetings of the CKPIPSRAA
</p>
<p class="subtitle has-text-info">Winding-up of CKPIPSRAA</p>
<p>
    In case of the winding-up of the CKPIPSRAA upon decision of the Chairman of CKPIPSRAA, any surplus fund or
    properties
    after meeting all the liabilities of the CKPIPSRAA shall be automatically comes under the ownership of the C. K.
    Pithawalla Institute of Pharmaceutical Science and Research, Surat to be utilized for the objectives inline with the
    objectives of the CKPIPSRAA.
</p>
